package com.study.student.vo;

public class StudentVO {

	private int stdNo;                      
	private String stdName;                 
	private String stdSex;                  
	private String stdBirth;
	
	public int getStdNo() {
		return stdNo;
	}
	public void setStdNo(int stdNo) {
		this.stdNo = stdNo;
	}
	public String getStdName() {
		return stdName;
	}
	public void setStdName(String stdName) {
		this.stdName = stdName;
	}
	public String getStdSex() {
		return stdSex;
	}
	public void setStdSex(String stdSex) {
		this.stdSex = stdSex;
	}
	public String getStdBirth() {
		return stdBirth;
	}
	public void setStdBirth(String stdBirth) {
		this.stdBirth = stdBirth;
	}                
	
}
